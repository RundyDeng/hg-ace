package com.jeefw.model.common;

import java.io.Serializable;
import javax.persistence.*;
import java.math.BigDecimal;
import java.util.Date;


/**
 * The persistent class for the TGZTJ database table.
 * 
 */
@Entity
@NamedQuery(name="Tgztj.findAll", query="SELECT t FROM Tgztj t")
public class Tgztj implements Serializable {
	private static final long serialVersionUID = 1L;

	private BigDecimal areaguid;

	@Temporal(TemporalType.DATE)
	private Date ddate;

	private BigDecimal dzbfsum;

	private BigDecimal gzsum;

	private BigDecimal totcbsum;

	private BigDecimal totzhsum;

	private BigDecimal wcbsum;

	private BigDecimal wfysum;

	private BigDecimal zcsum;

	public Tgztj() {
	}

	public BigDecimal getAreaguid() {
		return this.areaguid;
	}

	public void setAreaguid(BigDecimal areaguid) {
		this.areaguid = areaguid;
	}

	public Date getDdate() {
		return this.ddate;
	}

	public void setDdate(Date ddate) {
		this.ddate = ddate;
	}

	public BigDecimal getDzbfsum() {
		return this.dzbfsum;
	}

	public void setDzbfsum(BigDecimal dzbfsum) {
		this.dzbfsum = dzbfsum;
	}

	public BigDecimal getGzsum() {
		return this.gzsum;
	}

	public void setGzsum(BigDecimal gzsum) {
		this.gzsum = gzsum;
	}

	public BigDecimal getTotcbsum() {
		return this.totcbsum;
	}

	public void setTotcbsum(BigDecimal totcbsum) {
		this.totcbsum = totcbsum;
	}

	public BigDecimal getTotzhsum() {
		return this.totzhsum;
	}

	public void setTotzhsum(BigDecimal totzhsum) {
		this.totzhsum = totzhsum;
	}

	public BigDecimal getWcbsum() {
		return this.wcbsum;
	}

	public void setWcbsum(BigDecimal wcbsum) {
		this.wcbsum = wcbsum;
	}

	public BigDecimal getWfysum() {
		return this.wfysum;
	}

	public void setWfysum(BigDecimal wfysum) {
		this.wfysum = wfysum;
	}

	public BigDecimal getZcsum() {
		return this.zcsum;
	}

	public void setZcsum(BigDecimal zcsum) {
		this.zcsum = zcsum;
	}

}